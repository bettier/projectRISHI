/**
 * Set up map default view
 */
function initMap() {
  map = new google.maps.Map(document.getElementsByClassName('map')[0], {
    zoom: 14,
    minZoom: 13,
    disableDefaultUI: true,
    center: new google.maps.LatLng(30.565075, 77.516132),
    mapTypeId: 'roadmap',
    styles: mapStyles,
  });

  google.maps.event.addDomListener(window, 'resize', function () {
    var center = map.getCenter();
    google.maps.event.trigger(map, 'resize');
    map.setCenter(center);
  });

  var allowedBounds = new google.maps.LatLngBounds(
    new google.maps.LatLng(30.546687, 77.471524),
    new google.maps.LatLng(30.582681, 77.539492)
  );

  var lastValidCenter = map.getCenter();

  google.maps.event.addListener(map, 'center_changed', function () {
    if (!allowedBounds.contains(map.getCenter())) {
      map.panTo(lastValidCenter);

    } else {
      lastValidCenter = map.getCenter();
    }
  });
}


/**
 * Add marker to map
 */
function markerGenerator(name, coordinate, map, icon) {
  if (icon) {
    return new google.maps.Marker({
      position: coordinate,
      map: map,
      title: name,
      //info:
      animation: google.maps.Animation.DROP,
      icon: '../icons/' + icon + '.png'
    });
  }
  else {
    return new google.maps.Marker({
      position: coordinate,
      map: map,
      title: name,
      animation: google.maps.Animation.DROP
    });
  }
};

